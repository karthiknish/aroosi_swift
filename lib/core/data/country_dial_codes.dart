/// Lookup helper by dial code prefix (returns first match if duplicates like +1)
CountryDialCode? findCountryByDialCode(String dialCode) {
  return kCountryDialCodes.firstWhere(
    (c) => c.dialCode == dialCode,
    orElse: () => const CountryDialCode(
      value: 'United States',
      label: 'United States',
      dialCode: '+1',
      flag: 'ðŸ‡ºðŸ‡¸',
    ),
  );
}

/// Find by country name (case insensitive)
CountryDialCode? findCountryByName(String name) {
  final lower = name.toLowerCase();
  try {
    return kCountryDialCodes.firstWhere(
      (c) => c.value.toLowerCase() == lower || c.label.toLowerCase() == lower,
    );
  } catch (_) {
    return null;
  }
}
